
public class Actividad10 {
	public static void main (String[] arg) {
		
		//bucle
		for (int num_izq=1; num_izq<=10; num_izq++){
			for (int num_dere=1; num_dere<=10; num_dere++) {
				System.out.println(num_izq + " x " + num_dere + " = " + (num_izq * num_dere));
			}
			
			//Imprimir por pantalla las tablas 
			System.out.println();
		}
	}
	
}
