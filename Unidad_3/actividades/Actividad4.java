
public class Actividad4 {
	public static void main (String[] args) {
		//variables
		int x=1;
		
		//mostrar al usuario
		System.out.println("Antes del bucle");
		
		//bucle
		while (x<4) {
			System.out.println ("En el bucle");
			System.out.println ("El valor de x es"+x);
			
			x=x+1;
		}
		
		//mostrar al usuario
		System.out.println ("Despues del bucle");
	}
	

}
